#include <iostream>

using namespace std;

int main()
{
    int A, B, C;

    cin >> A;
    cin >> B;
    cin >> C;

    int somatorio = 0;

    somatorio = somatorio + A;
    somatorio = somatorio + B;
    somatorio = somatorio + C;

    cout << "O somatorio dos numeros e: " << somatorio << endl;

    return 0;
}
